package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalanKe=3;
        switch (jalanJalanKe){
            case 1:
                System.out.println("Jakarta");
                break;
            case 2:
                System.out.println("Banjarmasin");
                break;
            case 3:
                System.out.println("Yogyakarta");
                break;
            case 4:
                System.out.println("Surabaya");
                break;
            default:
                System.out.println("Ga Kemana Mana");
                break;
        }
    }
}
